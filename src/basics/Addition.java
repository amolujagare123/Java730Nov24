package basics;

public class Addition {

    public static void main(String[] args) {


        int a = 100 ;
        int b = 20;
        int c ;

        c = a + b;
        System.out.println("basics.Addition=" + c );

        c = a * b;
        System.out.println("Multiplication=" + c );

        c = a - b;
        System.out.println("Subtraction=" + c );

        c = a / b;
        System.out.println("Division=" + c );



    }
}
