package Arrays;

public class ArrayDemo2 {

    public static void main(String[] args) {

        double[] d = new double[5];

        d[0] = 34.51;
        d[1] = 14.26;
        d[2] = 38.53;
        d[3] = 74.46;
        d[4] = 94.55;

        for (int i=0;i<d.length; i++)
            System.out.println(d[i]);

    }
}
